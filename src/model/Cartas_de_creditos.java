package model;

public class Cartas_de_creditos {

    private int id;
    private int cota;
    private int grupo;
    private int proposta;
    private int prazo;
    private int parcela;
    private int valor_inicial;
    private int valor_final;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCota() {
        return cota;
    }

    public void setCota(int cota) {
        this.cota = cota;
    }

    public int getGrupo() {
        return grupo;
    }

    public void setGrupo(int grupo) {
        this.grupo = grupo;
    }

    public int getProposta() {
        return proposta;
    }

    public void setProposta(int proposta) {
        this.proposta = proposta;
    }

    public int getPrazo() {
        return prazo;
    }

    public void setPrazo(int prazo) {
        this.prazo = prazo;
    }

    public int getParcela() {
        return parcela;
    }

    public void setParcela(int parcela) {
        this.parcela = parcela;
    }

    public int getValor_inicial() {
        return valor_inicial;
    }

    public void setValor_inicial(int valor_inicial) {
        this.valor_inicial = valor_inicial;
    }

    public int getValor_final() {
        return valor_final;
    }

    public void setValor_final(int valor_final) {
        this.valor_final = valor_final;
    }
    
}
